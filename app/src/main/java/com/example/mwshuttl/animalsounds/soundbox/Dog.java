package com.example.mwshuttl.animalsounds.soundbox;

public abstract class Dog {

    /*
    Different languages have different sounds for different animals.

    For example, a dog's bark is "woof" in English and "ham" in Romanian. Other sounds:

    WOOF - English
    GAV - Russian
    WAOUH - French
    GUAU - Spanish
    BLAF - Dutch
    WAN - Japanese
    VOFF - Icelandic
    HAM - Romanian
    BAU - Italian
    WONG - Cantonese
    HEV - Turkish
    GUK - Indonesian
    HAAP - Persian
    MEONG - Korean
    */

    public abstract String bark();

}
